#include <unistd.h>

void	ft_putchar(char s[])
{
	write (1, &s[0], 1);
	write (1, &s[1], 1);
	write (1, &s[2], 1);
	if (!(s[0] == '7' && s[1] == '8' && s[2] == '9'))
	{
		write (1, ", ", 2);
	}
	s[2]++;
}

void	ft_print_comb(void)
{
	char	s[3];

	s[0] = '0';
	while (s[0] <= '7')
	{
		s[1] = s[0] + 1;
		while (s[1] <= '8')
		{
			s[2] = s[1] + 1;
			while (s[2] <= '9')
			{
				ft_putchar(s);
			}
			s[1]++;
		}
		s[0]++;
	}
}
